    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con=null;
        PreparedStatement ps=null;
        ResultSet rs=null;
        boolean matched=false;
        try{
                if(jTextField1.getText().equals("") || jTextField1.getText().isEmpty() || jPasswordField1.getText().equals("") || jPasswordField1.getText().isEmpty())
                {
                    JOptionPane.showMessageDialog(this, "Username or Password field is empty.", "Oops!", JOptionPane.ERROR_MESSAGE);
                }
                else
                {
                    String path=new File(".").getCanonicalPath();
                    Class.forName("org.sqlite.JDBC");
                    con=DriverManager.getConnection("jdbc:sqlite:"+ path +"\\chatbot.db");
                    ps=con.prepareStatement("SELECT * FROM `userinfo`");
                    rs=ps.executeQuery();
                    while(rs.next())
                    {
                        if(jTextField1.getText().equals(rs.getString("username")) && jPasswordField1.getText().equals(rs.getString("password")))
                        {
                            matched=true;
                            break;
                        }
                    }
                
                    if(matched)
                    {
                        this.dispose();
                        MainForm ob=new MainForm();
                        ob.setVisible(true);
                        ob.setResizable(false);
                        ob.setLocationRelativeTo(null);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Wrong username or password", "Oops!", JOptionPane.WARNING_MESSAGE);
                    }
                }
            }
        catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(this, "Problem to communicate with driver", "Error!", JOptionPane.ERROR_MESSAGE);
        }
        catch(ClassNotFoundException cnfe)
        {
            JOptionPane.showMessageDialog(this, "Can't find database driver", "Oops!", JOptionPane.ERROR_MESSAGE);
        }
            catch(IOException e)
            {
                System.out.println(e);
            }
            catch(NullPointerException npe)
            {
                JOptionPane.showMessageDialog(this, "All fields are mandetory!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        finally
        {
            try
            {
            con.close();
            ps.close();
            rs.close();
            }
            catch(Exception e)
            {
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

